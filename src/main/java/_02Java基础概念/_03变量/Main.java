package _02Java基础概念._03变量;

public class Main {

    // 变量: 在程序执行过程中, 其值有可能发生改变的量(数据)
    public static void main(String[] args) {

        // 变量的定义格式
            // 数据类型 变量名=数据值;
            // 数据类型: 限定了变量能存储数据的类型, int(整数), double(小数)...
            // 变量名: 就是存储空间的名字, 作用是方便以后使用！
            // 数据值: 真正存在变量中的数据

            // 等号: 赋值, 把右边的数据赋值给左边的变量！
        int a=11;
        System.out.println("a = " + a); // 11
    }
}

